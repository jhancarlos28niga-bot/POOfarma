    private void listarClientes() {

        DefaultTableModel modelo =(DefaultTableModel) jtableClientes.getModel();

        modelo.setRowCount(0);

        ClientesDAO dao = new ClientesDAO();

        List<Cliente> lista = dao.listar();

        for (Cliente clie : lista) 
        {

            Object[] fila = 
            {
                clie.getID_Cliente(),
                clie.getDNI(),
                clie.getNombre(),
                clie.getTelefono()
                
            };

            modelo.addRow(fila);
        }
    }
